package id.ac.ui.cs.advprog.subscriptionitemservice.model;

@Entity
@Table(name = "items")
public class Item {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(nullable = false)
    private String name;

    @ManyToOne(fetch = FetchType.LAZY)
    private SubscriptionBox subscriptionBox;


}